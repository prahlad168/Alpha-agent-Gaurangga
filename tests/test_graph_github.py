async def run_all_tests():
    """Run all tests."""
    print("\n" + "="*60)
    print("🤖 MAHALAKSMI AIOS v1.0.6 - Knowledge Graph & GitHub Tests")
    print("="*60)
    
    results = TestResults()
    
    try:
        # Knowledge Graph tests
        kg = await test_knowledge_graph_initialization(results)
        await test_graph_nodes_creation(results, kg)
        await test_graph_edges_creation(results, kg)
        await test_graph_company_relationships(results, kg)
        await test_graph_revenue_relationships(results, kg)
        await test_graph_product_relationships(results, kg)
        await test_graph_query(results, kg)
        
        # GitHub Center tests
        gh = await test_github_center_initialization(results)
        await test_github_status(results, gh)
        await test_github_branches(results, gh)
        await test_github_remote_info(results, gh)
        await test_github_conflict_detection(results, gh)
        await test_github_sync(results, gh)
        await test_github_history(results, gh)
        
    except Exception as e:
        print(f"\n❌ Test suite error: {e}")
        import traceback
        traceback.print_exc()
        results.failed += 1
    
    # Print summary
    print("\n" + "="*60)
    print(f"RESULTS: {results.passed} passed, {results.failed} failed")
    if results.errors:
        print("\nErrors:")
        for e in results.errors:
            print(f"  - {e}")
    print("="*60)
    
    success = results.failed == 0
    
    print("\n" + "="*60)
    if success:
        print("🎉 ALL TESTS PASSED!")
    else:
        print("⚠️ SOME TESTS FAILED - Review above")
    print("="*60)
    
    return success


if __name__ == "__main__":
    success = asyncio.run(run_all_tests())
    sys.exit(0 if success else 1)
